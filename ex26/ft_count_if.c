int		ft_count_if(char **tab, int (*f)(char*))
{
	int cpt;
	int cpt2;

	cpt = 0;
	cpt2 = 0;
	while (tab[cpt])
	{
		if ((*f)(tab[cpt]) == 1)
			cpt2++;
		cpt++;
	}
	return (cpt2);
}
